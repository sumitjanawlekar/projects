    private void assignJButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButton2ActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }
        

        trashCollectWorkRequest request = (trashCollectWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        request.setDeliveryBoyName(userAccount);
        if(request.getPickedUp().equalsIgnoreCase("Pickedup")){
            
        
        JOptionPane.showMessageDialog(null, "Cannot pick up this item");
        }
        else if(request.getPickedUp().equalsIgnoreCase("Delivered"))
        {
            JOptionPane.showMessageDialog(null, "Item already delivered,Please select another item");
        }
        else{
           request.setPickedUp("Pickedup");
        }
      
        populateTable();
    }//GEN-LAST:event_assignJButton2ActionPerformed

