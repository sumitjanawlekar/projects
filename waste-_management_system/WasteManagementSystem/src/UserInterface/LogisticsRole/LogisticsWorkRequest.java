    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : logisticsOrganization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = ((trashCollectWorkRequest)request);
            row[1] = request.getSender().getEmployee().getName();
            row[2] = ((trashCollectWorkRequest)request).getDeliveryBoyName() == null ? null : ((trashCollectWorkRequest)request).getDeliveryBoyName();
            row[3] = ((trashCollectWorkRequest)request).getPickedUp();
            
            model.addRow(row);
        }
    }
